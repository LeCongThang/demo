        }
    },
    methods: {
        async getProjectData(page) {
            this.$nuxt.$loading.start();
            let {
                data
            } = await axios.get(
                environment.apiUrl + "project?page=" + page
            );
            this.projectData = data.data;
            this.$nuxt.$loading.finish();
        }
    },
    mounted() {
        var url = $(location).attr("pathname");
        $("ul.nav > li").removeClass("active");
